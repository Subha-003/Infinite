-- =========================
CREATE TABLE DPT (
    DEPTNO INT PRIMARY KEY,
    DNAME VARCHAR(50),
    LOC VARCHAR(50)
);

CREATE TABLE EM (
    EMPNO INT PRIMARY KEY,
    ENAME VARCHAR(50),
    JOB VARCHAR(50),
    MGR INT NULL,
    HIREDATE DATE,
    SAL DECIMAL(10,2),
    COMM DECIMAL(10,2) NULL,
    DEPTNO INT,
    FOREIGN KEY (DEPTNO) REFERENCES DPT(DEPTNO)
);

