-- =========================

-- 1
SELECT * FROM EM WHERE ENAME LIKE 'A%';

-- 2
SELECT * FROM EM WHERE MGR IS NULL;

-- 3
SELECT ENAME, EMPNO, SAL 
FROM EM 
WHERE SAL BETWEEN 1200 AND 1400;

-- 4 BEFORE
SELECT * FROM EM WHERE DEPTNO = 20;

-- 4 UPDATE
UPDATE EM 
SET SAL = SAL * 1.10 
WHERE DEPTNO = 20;

-- 4 AFTER
SELECT * FROM EM WHERE DEPTNO = 20;

-- 5
SELECT COUNT(*) AS Clerk_Count 
FROM EM 
WHERE JOB = 'CLERK';

-- 6
SELECT JOB, AVG(SAL) AS Avg_Salary, COUNT(*) AS Employee_Count
FROM EM
GROUP BY JOB;

-- 7
SELECT * FROM EM
WHERE SAL = (SELECT MIN(SAL) FROM EM)
   OR SAL = (SELECT MAX(SAL) FROM EM);

-- 8
SELECT * 
FROM DPT D
WHERE NOT EXISTS (
    SELECT 1 
    FROM EM E 
    WHERE E.DEPTNO = D.DEPTNO
);

-- 9
SELECT ENAME, SAL 
FROM EM
WHERE JOB = 'ANALYST' 
  AND SAL > 1200 
  AND DEPTNO = 20
ORDER BY ENAME;

-- 10
SELECT D.DEPTNO, D.DNAME, SUM(E.SAL) AS Total_Salary
FROM DPT D
LEFT JOIN EM E ON D.DEPTNO = E.DEPTNO
GROUP BY D.DEPTNO, D.DNAME;

-- 11
SELECT ENAME, SAL 
FROM EM 
WHERE ENAME IN ('MILLER', 'SMITH');

-- 12
SELECT ENAME 
FROM EM 
WHERE ENAME LIKE 'A%' OR ENAME LIKE 'M%';

-- 13
SELECT ENAME, SAL * 12 AS Yearly_Salary 
FROM EM 
WHERE ENAME = 'SMITH';

-- 14
SELECT ENAME, SAL 
FROM EM 
WHERE SAL NOT BETWEEN 1500 AND 2850;

-- 15
SELECT MGR, COUNT(*) AS Report_Count
FROM EM
WHERE MGR IS NOT NULL
GROUP BY MGR
HAVING COUNT(*) > 2;